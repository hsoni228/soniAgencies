package soni.agencies.data;

/**
 *
 * @author YATRAONLINE\himanshu.soni
 */
public class ShopkeepersData {
    private String id;
    private String beat;
    private String name;
    private String mobileNo;
    private String gstNo;
    private String adhaarNo;
    private String panNo;
    private String compositionNo;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getBeat() {
        return beat;
    }

    public void setBeat(String beat) {
        this.beat = beat;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobileNo() {
        return mobileNo;
    }

    public void setMobileNo(String mobileNo) {
        this.mobileNo = mobileNo;
    }

    public String getGstNo() {
        return gstNo;
    }

    public void setGstNo(String gstNo) {
        this.gstNo = gstNo;
    }

    public String getAdhaarNo() {
        return adhaarNo;
    }

    public void setAdhaarNo(String adhaarNo) {
        this.adhaarNo = adhaarNo;
    }

    public String getPanNo() {
        return panNo;
    }

    public void setPanNo(String panNo) {
        this.panNo = panNo;
    }

    public String getCompositionNo() {
        return compositionNo;
    }

    public void setCompositionNo(String compositionNo) {
        this.compositionNo = compositionNo;
    }
}
